num = 42

# Operações com tipos numéricos:
print(5 + 4)
print(7 - 2)
print(3 * 5)
print(5 / 2)

# E se eu quiser o inteiro da divisã de 5 por 2?
divisao = int(5 / 2)
print(divisao)

# Em Python para divisão retornar somente o valor inteiro não precisamos fazer cast, basta usar //:
divisao = 5 // 2
print(divisao)

# Podemos também descobrir qual é o resto da divisão usando %:
print(4 % 2)
print(5 % 2)
